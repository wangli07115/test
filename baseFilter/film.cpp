void film(unsigned char *image, int width, int height, int depth, int stride) {
    register int i, j;
    register unsigned char *pBuf = NULL;
    register int r = 0, g = 0, b = 0;

    for(j = 0; j < height; j++) {
        pBuf = image + j * stride;
        for(i = 0; i < width; i++, pBuf += depth) {
            r = PR(pBuf);
            g = PG(pBuf);
            b = PB(pBuf);
            PR(pBuf) = *(gFilterFilm_RGTable + r);
            PG(pBuf) = *(gFilterFilm_RGTable + g);
            PB(pBuf) = *(gFilterFilm_BTable + b);
        }
    }

}
