void filters_film(unsigned char *image, int width, int height)
{
	register int i = width * height;
	register unsigned char * dp = NULL;
	register int r = 0, g = 0, b = 0;

    dp = image;
	while (i--) {
		if (PA(dp)) {
			r = PR(dp);
			g = PG(dp);
			b = PB(dp);

			r = *(gFilterFilm_RGTable + r);
			g = *(gFilterFilm_RGTable + g);
			b = *(gFilterFilm_BTable + b);

			PR(dp) = r;
			PG(dp) = g;
			PB(dp) = b;
		}
		dp += 4;
	}
}
